

# Arithmetic operators (+),(-),(*),(/),(//),(**),(%)

a=90
b=7

print(a+b)
print(a-b)
print(a*b)
# When we use (/) we get output as a float value like 5.98000010...
print(a/b)
print(a%b)

# When we use (//) we get floor int value like 5
print(a//b)

# (**) mean power calculate

print(2**2)
print(2**3)

